class KvasirDataset(Dataset):
    def __init__(self, df, augment=False, size=224):
        self.df = df.reset_index(drop=True)
        self.size = size
        self.augment = augment

        if augment:
            self.transform = A.Compose([
                A.Resize(self.size, self.size),
                A.HorizontalFlip(p=0.5),
                A.VerticalFlip(p=0.5),
                A.RandomRotate90(p=0.5),
                A.Normalize()
            ])
        else:
            self.transform = A.Compose([
                A.Resize(self.size, self.size),
                A.Normalize()
            ])

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        img_path = row["image"]
        mask_path = row["mask"]

        img = cv2.cvtColor(cv2.imread(img_path), cv2.COLOR_BGR2RGB)
        mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
        mask = np.expand_dims(mask, -1)

        data = self.transform(image=img, mask=mask)
        img, mask = data["image"], data["mask"]

        img = np.transpose(img, (2, 0, 1)).astype("float32")
        mask = np.transpose(mask, (2, 0, 1)).astype("float32") / 255.0
        return torch.tensor(img), torch.tensor(mask)

class BiFusionBlock(nn.Module):
    def __init__(self, cnn_ch, trans_ch, out_ch):
        super().__init__()
        self.conv_cnn = nn.Conv2d(cnn_ch, out_ch, 1)
        self.conv_trans = nn.Conv2d(trans_ch, out_ch, 1)
        self.conv_out = nn.Conv2d(out_ch, out_ch, 3, padding=1)
        self.act = nn.ReLU(inplace=True)

    def forward(self, cnn_feat, trans_feat):
        # ✅ Ensure transformer feature is [B, C, H, W]
        if trans_feat.ndim == 4 and trans_feat.shape[1] < 10:
            trans_feat = trans_feat.permute(0, 3, 1, 2)

        c = self.conv_cnn(cnn_feat)
        t = self.conv_trans(trans_feat)

        x = c + t
        x = self.act(self.conv_out(x))

        return x


class TransFuseSimple(nn.Module):
    def __init__(self, num_classes=1, pretrained=True):
        super().__init__()
        self.cnn = timm.create_model("efficientnet_b0", pretrained=pretrained, features_only=True)
        self.trans = timm.create_model("mobilenetv3_large_100", pretrained=pretrained, features_only=True)

        cnn_chs = self.cnn.feature_info.channels()
        trans_chs = self.trans.feature_info.channels()

        self.fuse3 = BiFusionBlock(cnn_chs[-1], trans_chs[-1], 256)
        self.fuse2 = BiFusionBlock(cnn_chs[-2], trans_chs[-2], 128)
        self.fuse1 = BiFusionBlock(cnn_chs[-3], trans_chs[-3], 64)

        self.conv_final = nn.Conv2d(64 + 128 + 256, num_classes, 1)

    def forward(self, x):
        cnn_feats = self.cnn(x)
        trans_feats = self.trans(x)

        # 🔥 FIX ALL transformer features (CORRECT LOCATION)
        fixed_trans_feats = []
        for t in trans_feats:
            if t.ndim == 4 and t.shape[-1] > t.shape[1]:
                t = t.permute(0, 3, 1, 2)
            fixed_trans_feats.append(t)

        trans_feats = fixed_trans_feats

        # 🔥 FUSION
        f3 = self.fuse3(cnn_feats[-1], trans_feats[-1])
        f2 = self.fuse2(cnn_feats[-2], trans_feats[-2])
        f1 = self.fuse1(cnn_feats[-3], trans_feats[-3])

        f3_up = F.interpolate(f3, size=f1.shape[2:], mode="bilinear", align_corners=False)
        f2_up = F.interpolate(f2, size=f1.shape[2:], mode="bilinear", align_corners=False)

        cat = torch.cat([f3_up, f2_up, f1], dim=1)
        out = self.conv_final(cat)

        return torch.sigmoid(out)

