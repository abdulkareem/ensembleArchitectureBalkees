for i in random.sample(range(len(val_ds)), n_samples):
    img, mask = val_ds[i]
    x = img.unsqueeze(0).to(DEVICE)

    with torch.no_grad():
        pred = model(x)
        pred = F.interpolate(pred, size=mask.shape[1:], mode="bilinear", align_corners=False)
        pred = (pred > 0.5).float()[0][0].cpu().numpy()

    fig, ax = plt.subplots(1, 3, figsize=(12, 4))
    ax[0].imshow(np.transpose(img, (1, 2, 0))); ax[0].set_title("Original Image"); ax[0].axis('off')
    ax[1].imshow(mask[0], cmap="gray"); ax[1].set_title("Ground Truth"); ax[1].axis('off')
    ax[2].imshow(pred, cmap="gray"); ax[2].set_title("Predicted Mask"); ax[2].axis('off')

    # Save to Drive
    save_path = os.path.join(save_dir, f"transfuse_prediction_{i+1}.png")
    plt.savefig(save_path, bbox_inches="tight", dpi=150)
    plt.close(fig)

    print(f"🖼️ Saved prediction: {save_path}")

