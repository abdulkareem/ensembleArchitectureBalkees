optimizer = torch.optim.Adam(model.parameters(), lr=1e-4)

# ----- Metric functions (your requested full set) -----
def dice(pred, mask):
    pred = pred.reshape(-1)
    mask = mask.reshape(-1)
    inter = (pred * mask).sum()
    return (2*inter + 1e-6) / (pred.sum() + mask.sum() + 1e-6)

def iou(pred, mask):
    pred = pred.reshape(-1)
    mask = mask.reshape(-1)
    inter = (pred * mask).sum()
    union = pred.sum() + mask.sum() - inter
    return (inter + 1e-6) / (union + 1e-6)

def pixel_accuracy(pred, mask):
    pred = pred.reshape(-1)
    mask = mask.reshape(-1)
    correct = (pred == mask).sum()
    total = mask.numel()
    return correct.float() / total

def sensitivity(pred, mask):
    pred = pred.reshape(-1)
    mask = mask.reshape(-1)
    tp = ((pred == 1) & (mask == 1)).sum()
    fn = ((pred == 0) & (mask == 1)).sum()
    return (tp + 1e-6) / (tp + fn + 1e-6)

def specificity(pred, mask):
    pred = pred.reshape(-1)
    mask = mask.reshape(-1)
    tn = ((pred == 0) & (mask == 0)).sum()
    fp = ((pred == 0) & (mask == 1)).sum()
    return (tn + 1e-6) / (tn + fp + 1e-6)

from tqdm.auto import tqdm

EPOCHS = 50
best_val_dice = 0.0
save_path = "/content/best_resunetpp_model.pth"

history = {
    "epoch": [], "train_loss": [],
    "val_dice": [], "val_iou": [], "val_pixel_acc": [],
    "val_sensitivity": [], "val_specificity": []
}

for ep in range(1, EPOCHS + 1):
    model.train()
    epoch_loss = 0
    loop = tqdm(train_loader, desc=f"Epoch {ep}/{EPOCHS}")
    for x, y in loop:
        x, y = x.to(DEVICE), y.to(DEVICE)
        optimizer.zero_grad()
        out = model(x)
        loss = dice_loss(out, y) + bce_loss(out, y)
        loss.backward()
        optimizer.step()
        epoch_loss += loss.item()
        loop.set_postfix({"loss": f"{loss.item():.4f}"})
    train_loss = epoch_loss / len(train_loader)

    # ----- Validation -----
    model.eval()
    dices=[]; ious=[]; accs=[]; sens=[]; specs=[]
    with torch.no_grad():
        for x, y in val_loader:
            x, y = x.to(DEVICE), y.to(DEVICE)
            preds = (torch.sigmoid(model(x)) > 0.5).float()
            y_bin = y[:, 0:1, :, :]
            p_bin = preds[:, 0:1, :, :]

            dices.append(dice(p_bin, y_bin).item())
            ious.append(iou(p_bin, y_bin).item())
            accs.append(pixel_accuracy(p_bin, y_bin).item())
            sens.append(sensitivity(p_bin, y_bin).item())
            specs.append(specificity(p_bin, y_bin).item())

    history["epoch"].append(ep)
    history["train_loss"].append(train_loss)
    history["val_dice"].append(np.mean(dices))
    history["val_iou"].append(np.mean(ious))
    history["val_pixel_acc"].append(np.mean(accs))
    history["val_sensitivity"].append(np.mean(sens))
    history["val_specificity"].append(np.mean(specs))

    print(f"✅ Epoch {ep} | Loss {train_loss:.4f} | Dice {np.mean(dices):.4f} | IoU {np.mean(ious):.4f}")

    if np.mean(dices) > best_val_dice:
        best_val_dice = np.mean(dices)
        torch.save(model.state_dict(), save_path)
        print(f"💾 Saved Best Model (Dice: {best_val_dice:.4f})")

