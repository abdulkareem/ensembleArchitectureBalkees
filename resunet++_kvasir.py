class KvasirDataset(Dataset):
    def __init__(self, df, aug=None, prep=None):
        self.df = df.reset_index(drop=True)
        self.aug = aug
        self.prep = prep

    def __len__(self): return len(self.df)

    def __getitem__(self, idx):
        img = cv2.cvtColor(cv2.imread(self.df.iloc[idx,0]), cv2.COLOR_BGR2RGB)
        mask = cv2.imread(self.df.iloc[idx,1], cv2.IMREAD_GRAYSCALE)
        mask = (mask > 127).astype("float32")
        mask = np.expand_dims(mask, axis=-1)

        if self.aug:
            data = self.aug(image=img, mask=mask)
            img, mask = data["image"], data["mask"]

        if self.prep:
            data = self.prep(image=img, mask=mask)
            img, mask = data["image"], data["mask"]

        return img, mask

def aug_train():
    return A.Compose([
        A.Resize(512,512),
        A.HorizontalFlip(),
        A.VerticalFlip(),
        A.RandomRotate90(p=0.5),
    ])

def aug_val():
    return A.Compose([A.Resize(512,512)])

prep_fn = smp.encoders.get_preprocessing_fn("resnet50", "imagenet")
def prep():
    return A.Compose([
        A.Lambda(image=prep_fn),
        A.Lambda(
            image=lambda x, **k: x.transpose(2,0,1).astype("float32"),
            mask=lambda x, **k: x.transpose(2,0,1).astype("float32")
        ),
    ])

train_ds = KvasirDataset(train_df, aug_train(), prep())
val_ds   = KvasirDataset(val_df, aug_val(), prep())
holdout_ds = KvasirDataset(valid_df, aug_val(), prep())

train_loader = DataLoader(train_ds, batch_size=4, shuffle=True, num_workers=2)
val_loader   = DataLoader(val_ds, batch_size=4, shuffle=False, num_workers=2)
