class KvasirDataset(Dataset):
    def __init__(self, df, size=512, augment=False):
        self.df = df.reset_index(drop=True)
        self.size = size
        self.augment = augment
        if augment:
            self.tf = A.Compose([
                A.Resize(size, size),
                A.HorizontalFlip(p=0.5),
                A.VerticalFlip(p=0.2),
                A.RandomBrightnessContrast(p=0.2),
                A.Normalize()
            ])
        else:
            self.tf = A.Compose([A.Resize(size, size), A.Normalize()])

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        r = self.df.iloc[idx]
        img_path, mask_path = r['image'], r['mask']

        if not os.path.exists(img_path) or not os.path.exists(mask_path):
            raise FileNotFoundError(f"Missing: {img_path} or {mask_path}")

        img = cv2.cvtColor(cv2.imread(img_path), cv2.COLOR_BGR2RGB)
        mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
        mask = np.expand_dims(mask, -1)

        sample = self.tf(image=img, mask=mask)
        img = sample['image'].astype('float32').transpose(2,0,1)
        mask = (sample['mask'].astype('float32') / 255.0).transpose(2,0,1)
        return torch.tensor(img), torch.tensor(mask)

class ObjectAwareAttention(nn.Module):
    def __init__(self, channels):
        super().__init__()
        self.channel_att = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(channels, max(1, channels//8), 1),
            nn.ReLU(True),
            nn.Conv2d(max(1, channels//8), channels, 1),
            nn.Sigmoid()
        )
        self.spatial_conv = nn.Sequential(nn.Conv2d(2, 1, 3, padding=1), nn.Sigmoid())

    def forward(self, feat, coarse=None):
        ca = self.channel_att(feat) * feat
        if coarse is None:
            mp = torch.max(feat, dim=1, keepdim=True)[0]
            ap = torch.mean(feat, dim=1, keepdim=True)
            sm = self.spatial_conv(torch.cat([mp, ap], dim=1))
        else:
            sm = F.interpolate(coarse, size=feat.shape[2:], mode='bilinear', align_corners=False)
        return feat * ca * sm


class WeightedFusion(nn.Module):
    def __init__(self, channels):
        super().__init__()
        self.convA = nn.Conv2d(channels, channels, 1)
        self.convB = nn.Conv2d(channels, channels, 1)
        self.weight_conv = nn.Sequential(
            nn.Conv2d(channels*2, channels, 3, padding=1),
            nn.ReLU(True),
            nn.Conv2d(channels, channels, 1),
            nn.Sigmoid()
        )
    def forward(self, A, B):
        a, b = self.convA(A), self.convB(B)
        w = self.weight_conv(torch.cat([a, b], 1))
        return a*w + b*(1-w)


class WDFFNet(nn.Module):
    def __init__(self, pretrained=True, num_classes=1):
        super().__init__()
        self.backA = timm.create_model('efficientnet_b0', pretrained=pretrained,
                                       features_only=True, out_indices=(1,2,3,4))
        self.backB = timm.create_model('resnet50', pretrained=pretrained,
                                       features_only=True, out_indices=(1,2,3,4))

        chsA = self.backA.feature_info.channels()   # [24, 40, 112, 320]
        chsB = self.backB.feature_info.channels()   # [256, 512, 1024, 2048]
        target_chs = [64, 96, 128, 160]

        self.projA = nn.ModuleList([nn.Conv2d(ca, tc, 1) for ca, tc in zip(chsA, target_chs)])
        self.projB = nn.ModuleList([nn.Conv2d(cb, tc, 1) for cb, tc in zip(chsB, target_chs)])

        self.fusions = nn.ModuleList([WeightedFusion(tc) for tc in target_chs])
        self.oams    = nn.ModuleList([ObjectAwareAttention(tc) for tc in target_chs])

        # Decoder
        self.up3 = nn.ConvTranspose2d(160, 128, 2, stride=2)
        self.up2 = nn.ConvTranspose2d(128, 96, 2, stride=2)
        self.up1 = nn.ConvTranspose2d(96, 64, 2, stride=2)

        self.dec_conv3 = nn.Sequential(
            nn.Conv2d(128+128, 128, 3, padding=1),
            nn.ReLU(True)
        )
        self.dec_conv2 = nn.Sequential(
            nn.Conv2d(96+96, 96, 3, padding=1),
            nn.ReLU(True)
        )
        self.dec_conv1 = nn.Sequential(
            nn.Conv2d(64+64, 64, 3, padding=1),
            nn.ReLU(True)
        )

        # FINAL output only at the end
        self.out_conv = nn.Conv2d(64, num_classes, 1)

    def forward(self, x):
        featsA, featsB = self.backA(x), self.backB(x)
        fused = []

        # Encoder: remove coarse prediction
        for pa, pb, fa, fb, fus, oam in zip(self.projA, self.projB, featsA, featsB, self.fusions, self.oams):
            f = fus(pa(fa), pb(fb))
            f = oam(f, None)          # no coarse guidance
            fused.append(f)

        f1, f2, f3, f4 = fused  # shallow → deep

        # Decoder
        u3 = self.up3(f4)
        d3 = self.dec_conv3(torch.cat([u3, f3], 1))

        u2 = self.up2(d3)
        d2 = self.dec_conv2(torch.cat([u2, f2], 1))

        u1 = self.up1(d2)
        d1 = self.dec_conv1(torch.cat([u1, f1], 1))

        return torch.sigmoid(self.out_conv(d1))

