import numpy as np
from tqdm import tqdm

def dice(p,g): inter=(p*g).sum(); return (2*inter+1e-6)/(p.sum()+g.sum()+1e-6)
def iou(p,g): inter=(p*g).sum(); union=p.sum()+g.sum()-inter; return (inter+1e-6)/(union+1e-6)
def pixel_acc(p,g): return (p==g).float().mean()
def sens(p,g): tp=((p==1)&(g==1)).sum(); fn=((p==0)&(g==1)).sum(); return (tp+1e-6)/(tp+fn+1e-6)
def spec(p,g): tn=((p==0)&(g==0)).sum(); fp=((p==1)&(g==0)).sum(); return (tn+1e-6)/(tn+fp+1e-6)

EPOCHS = 50
best = 0.0
history = {k:[] for k in ["epoch","train_loss","val_dice","val_iou","val_acc","val_sens","val_spec"]}

for ep in range(1, EPOCHS+1):
    model.train()
    run_loss = 0.0
    for imgs, masks in tqdm(train_loader, desc=f"Epoch {ep}/{EPOCHS} - Train"):
        imgs, masks = imgs.to(DEVICE), masks.to(DEVICE)
        optimizer.zero_grad()
        out = model(imgs)
        if out.shape != masks.shape:
            out = F.interpolate(out, size=masks.shape[2:], mode='bilinear', align_corners=False)
        loss = criterion(out, masks)
        loss.backward()
        optimizer.step()
        run_loss += loss.item()
    train_loss = run_loss / len(train_loader)

    # Validation
    model.eval()
    dices, ious, accs, senss, specs = [], [], [], [], []
    with torch.no_grad():
        for imgs, masks in tqdm(val_loader, desc=f"Epoch {ep}/{EPOCHS} - Val"):
            imgs, masks = imgs.to(DEVICE), masks.to(DEVICE)
            out = model(imgs)
            out = F.interpolate(out, size=masks.shape[2:], mode='bilinear', align_corners=False)
            preds = (out > 0.5).float()
            for p,g in zip(preds, masks):
                dices.append(dice(p,g).item())
                ious.append(iou(p,g).item())
                accs.append(pixel_acc(p,g).item())
                senss.append(sens(p,g).item())
                specs.append(spec(p,g).item())

    vd, vi, va, vsn, vsp = np.mean(dices), np.mean(ious), np.mean(accs), np.mean(senss), np.mean(specs)
    history["epoch"].append(ep); history["train_loss"].append(train_loss)
    history["val_dice"].append(vd); history["val_iou"].append(vi)
    history["val_acc"].append(va); history["val_sens"].append(vsn); history["val_spec"].append(vsp)

    print(f"✅ Epoch {ep} | Loss {train_loss:.4f} | Dice {vd:.4f} | IoU {vi:.4f} | Acc {va:.4f} | Sens {vsn:.4f} | Spec {vsp:.4f}")

